#include "libft.h"

void		*ft_memset(void *b, int c, size_t n)
{
	size_t	i;
	char	*str;

	i = 0;
	str = (char *)b;
	while (i < n)
	{
		str[i] = c;
		i++;
	}
	return (b);
}
